'''
Tipos int e float
int -> número inteiro
O tipo int representa qualquer número positivo ou negativo
int sem sinal é considerado positivo
'''
print(11)
print(-11)
print(0)


'''
float -> Número com ponto flutuante
O tipo float representa qualquer numero positivo ou negativo com ponto flutuante
float sem sinal é considerado positivo
'''
print(1.1) # float
print(0.0) # float
print(-5.1) # float

# A função tupe mostra o tipo que o python inferiu ao valor
print( type('wander'))
print( type(1))
print( type(1.1))

