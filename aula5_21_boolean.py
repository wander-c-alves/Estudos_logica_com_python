'''
Tipo de dado bool (boolean)
Ao questionar algo em um programa, só existe duas respostas possiveis:
sim(True) ou não(False).
Existem varios operadores para "questionar".
Dentre eles, o ==, que é um operador logico que questiona se um valor é igual ao outro.

'''
print(10 == 10) # Sim => True => Verdadeiro
print(10 == 11) # NÃO => False => Falso
print(type(10 == 10))
print(type(10 == 11))

